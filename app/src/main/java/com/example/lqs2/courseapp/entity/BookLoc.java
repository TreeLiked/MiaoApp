package com.example.lqs2.courseapp.entity;

public class BookLoc {

    private String searchNo;
    private String idNo;
    private String yearNo;
    private String roomNo;
    private String blState;


    public String getSearchNo() {
        return searchNo;
    }

    public void setSearchNo(String searchNo) {
        this.searchNo = searchNo;
    }

    public String getIdNo() {
        return idNo;
    }

    public void setIdNo(String idNo) {
        this.idNo = idNo;
    }

    public String getYearNo() {
        return yearNo;
    }

    public void setYearNo(String yearNo) {
        this.yearNo = yearNo;
    }

    public String getRoomNo() {
        return roomNo;
    }

    public void setRoomNo(String roomNo) {
        this.roomNo = roomNo;
    }

    public String getBlState() {
        return blState;
    }

    public void setBlState(String blState) {
        this.blState = blState;
    }
}
